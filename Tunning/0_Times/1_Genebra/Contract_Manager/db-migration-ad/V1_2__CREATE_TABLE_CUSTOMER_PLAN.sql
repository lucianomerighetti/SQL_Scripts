create TABLE AD.customer_plan (
    IDT_CUSTOMER_PLAN               NUMBER(20)
     NOT NULL,
	COD_CUSTOMER					VARCHAR(50)				    NOT NULL,
	COD_CONTRACT					VARCHAR(50)				    NOT NULL,
    IND_PAYMENT_PROCESSOR           VARCHAR(30)                 NOT NULL,
    IND_STATUS                      NUMBER(2)                   NOT NULL,
    DAT_CREATION                    TIMESTAMP DEFAULT SYSDATE   NOT NULL,
    DAT_UPDATE                      TIMESTAMP DEFAULT SYSDATE   NOT NULL,
    CONSTRAINT CUSTOMER_PLAN_PK PRIMARY KEY (IDT_CUSTOMER_PLAN)
    USING INDEX TABLESPACE TSICONTRACTMANAGER01 INITRANS 10
) TABLESPACE TSDCONTRACTMANAGER01 INITRANS 10;

COMMENT ON TABLE  AD.CUSTOMER_PLAN                                       IS 'Tabela de controle de planos de usuários para serem criados contratos';
COMMENT ON COLUMN AD.CUSTOMER_PLAN.IDT_CUSTOMER_PLAN                     IS 'Identificador da tabela';
COMMENT ON COLUMN AD.CUSTOMER_PLAN.COD_CUSTOMER                          IS 'Código do lojista';
COMMENT ON COLUMN AD.CUSTOMER_PLAN.COD_CONTRACT                          IS 'Código do contrato';
COMMENT ON COLUMN AD.CUSTOMER_PLAN.IND_PAYMENT_PROCESSOR                 IS 'Bandeira do cartão de crédito';
COMMENT ON COLUMN AD.CUSTOMER_PLAN.IND_STATUS                            IS 'Status do envio do contrato à CERC';
COMMENT ON COLUMN AD.CUSTOMER_PLAN.DAT_CREATION                          IS 'Data de criação do contrato';
COMMENT ON COLUMN AD.CUSTOMER_PLAN.DAT_UPDATE                            IS 'Data de atualização do contrato';

ALTER TABLE AD.CUSTOMER_PLAN ADD CONSTRAINT CUSTOMER_PLAN_COD_CONTRACT_FK FOREIGN KEY ( COD_CONTRACT )
        REFERENCES AD.CONTRACT_CUSTOMER ( COD_CONTRACT );

GRANT SELECT,UPDATE,INSERT,DELETE ON AD.CUSTOMER_PLAN TO CONTRACTMANAGERUBR;
GRANT SELECT ON AD.CUSTOMER_PLAN TO MANUT_CONTRACTMANAGER;
