CREATE TABLE RECEIVABLES_ADM.CASHOUT (
    IDT_CASHOUT                 NUMBER(20) GENERATED ALWAYS AS IDENTITY INCREMENT BY 1 MAXVALUE 9999999999999999999999999999 MINVALUE 1 CACHE 1000 NOCYCLE NOORDER NOT NULL,
    IDT_CONTRACTUAL_EFFECT      NUMBER(20) NOT NULL,
    COD_CASHOUT                 VARCHAR2(36 BYTE) NOT NULL,
    NUM_AMOUNT                  NUMBER(11) NOT NULL,
    IND_STATUS                  VARCHAR2(50 BYTE) NOT NULL,
    DAT_CASHOUT_CONFIRMATION    TIMESTAMP NULL,
    DAT_CREATION                TIMESTAMP NOT NULL,
    DAT_UPDATE                  TIMESTAMP NOT NULL,
    CONSTRAINT CASHOUT_PK PRIMARY KEY ( IDT_CASHOUT ) USING INDEX TABLESPACE TSIRECEIVABLES01 INITRANS 48
) TABLESPACE TSDRECEIVABLES01 INITRANS 32
;

COMMENT ON TABLE RECEIVABLES_ADM.CASHOUT IS '[NOT_SECURITY_APPLY] Tabela para controle de cashout autorizados e confirmados no processo de liquidação';

COMMENT ON COLUMN RECEIVABLES_ADM.CASHOUT.IDT_CASHOUT IS '[NOT_SECURITY_APPLY] Identificador da entidade';

COMMENT ON COLUMN RECEIVABLES_ADM.CASHOUT.IDT_CONTRACTUAL_EFFECT IS '[NOT_SECURITY_APPLY] Identificador do efeito de contrato dono do Cashout';
	
COMMENT ON COLUMN RECEIVABLES_ADM.CASHOUT.COD_CASHOUT IS '[NOT_SECURITY_APPLY] Código do cashout';

COMMENT ON COLUMN RECEIVABLES_ADM.CASHOUT.NUM_AMOUNT IS '[NOT_SECURITY_APPLY] Valor total do cashout';

COMMENT ON COLUMN RECEIVABLES_ADM.CASHOUT.IND_STATUS IS '[NOT_SECURITY_APPLY] Status da entidade cashout';

COMMENT ON COLUMN RECEIVABLES_ADM.CASHOUT.DAT_CASHOUT_CONFIRMATION IS '[NOT_SECURITY_APPLY] Data que o cashout foi confirmado para domínio de Cashout do PagSeguro';

COMMENT ON COLUMN RECEIVABLES_ADM.CASHOUT.DAT_CREATION IS '[NOT_SECURITY_APPLY] Data de criação';

COMMENT ON COLUMN RECEIVABLES_ADM.CASHOUT.DAT_UPDATE IS '[NOT_SECURITY_APPLY] Data de atualização';

-- Unique
ALTER TABLE RECEIVABLES_ADM.CASHOUT ADD CONSTRAINT CASHOUT_UK01 UNIQUE ( COD_CASHOUT ) USING INDEX TABLESPACE TSIRECEIVABLES01 INITRANS 12 ENABLE NOVALIDATE;


-- FK with index
ALTER TABLE RECEIVABLES_ADM.CASHOUT ADD CONSTRAINT CASHOUT_IDT_CON_EFFE_FK FOREIGN KEY ( IDT_CONTRACTUAL_EFFECT )
        REFERENCES RECEIVABLES_ADM.CONTRACTUAL_EFFECT ( IDT_CONTRACTUAL_EFFECT ) ENABLE NOVALIDATE;

